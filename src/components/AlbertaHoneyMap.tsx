// Project lat/lon to SVG coordinates
function projectCoordinate(lon: number, lat: number): [number, number] {
  const x = ((lon - ALBERTA_BOUNDS.minLon) / (ALBERTA_BOUNDS.maxLon - ALBERTA_BOUNDS.minLon)) * SVG_W;
  const y = SVG_H - ((lat - ALBERTA_BOUNDS.minLat) / (ALBERTA_BOUNDS.maxLat - ALBERTA_BOUNDS.minLat)) * SVG_H;
  return [x, y];
}


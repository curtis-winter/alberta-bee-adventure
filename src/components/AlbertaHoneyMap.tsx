export default function AlbertaHoneyMap({}: AlbertaHoneyMapProps) {
  const [hoveredRegion, setHoveredRegion] = useState<string | null>(null);
  const [selectedRegion, setSelectedRegion] = useState<string | null>(null);

  const outlinePoints = ALBERTA_OUTLINE.map(([lon, lat]) => {
    const [x, y] = projectCoordinate(lon, lat);
    return `${x},${y}`;
  }).join(' ');

  const selected = selectedRegion ? honeyRegions.find(r => r.id === selectedRegion) : null;
  const hovered = hoveredRegion ? honeyRegions.find(r => r.id === hoveredRegion) : null;

  const legendItems = [
    { color: '#e6194b', label: 'Peace', desc: '3,232 t/yr' },
    { color: '#3cb44b', label: 'North West', desc: '4,386 t/yr' },
    { color: '#ffe119', label: 'North East', desc: '2,432 t/yr' },
    { color: '#4363d8', label: 'Central', desc: '1,646 t/yr' },
    { color: '#f58231', label: 'South', desc: '3,755 t/yr' },
  ];

  return (
    <div className="w-full max-w-4xl mx-auto p-4">
      <div className="text-center mb-6">
        <h3 className="text-3xl lg:text-4xl font-black uppercase tracking-tighter mb-2">
          Alberta Honey Production
        </h3>
        <p className="text-lg font-bold text-stone-600">
          Click a region to see production details
        </p>
      </div>

      <div className="grid md:grid-cols-2 gap-6">
        <div className="bg-transparent p-4 flex justify-center">
          <svg
            viewBox={`0 0 ${SVG_W} ${SVG_H}`}
            className="w-full h-auto"
          >
            <defs>
              <clipPath id="albertaClip">
                <polygon points={outlinePoints} />
              </clipPath>
            </defs>
            
            {/* Background */}
            <g clipPath="url(#albertaClip)">
              <rect x="0" y="0" width={SVG_W} height={SVG_H} fill="transparent" />
            </g>

            {/* Region polygons */}
            <g clipPath="url(#albertaClip)">
              {honeyRegions.map((region) => {
                const isActive = hoveredRegion === region.id || selectedRegion === region.id;
                const points = region.coords.map(([lon, lat]) => {
                  const [x, y] = projectCoordinate(lon, lat);
                  return `${x},${y}`;
                }).join(' ');
                
                return (
                  <polygon
                    key={region.id}
                    points={points}
                    fill={region.color}
                    stroke="#000"
                    strokeWidth={isActive ? 4 : 1.5}
                    className="cursor-pointer transition-all duration-200"
                    onMouseEnter={() => setHoveredRegion(region.id)}
                    onMouseLeave={() => setHoveredRegion(null)}
                    onClick={() => setSelectedRegion(
                      selectedRegion === region.id ? null : region.id
                    )}
                  />
                );
              })}
            </g>

            {/* Province outline */}
            <polygon
              points={outlinePoints}
              fill="none"
              stroke="#6b7280"
              strokeWidth={3}
            />
          </svg>

          
        </div>

        {/* Legend and Info Panel */}
        <div className="flex flex-col gap-4">
          {/* Legend */}
          <div className="brutalist-card p-4 bg-white">
            <h4 className="text-lg font-black uppercase mb-3">Production Level</h4>
            <div className="space-y-2">
              {legendItems.map((item, i) => (
                <div key={i} className="flex items-center gap-3">
                  <div 
                    className="w-6 h-6 border-2 border-black" 
                    style={{ backgroundColor: item.color }}
                  />
                  <div>
                    <span className="font-bold text-sm">{item.label}</span>
                    <span className="text-xs text-stone-500 ml-1">- {item.desc}</span>
                  </div>
                </div>
              ))}
            </div>
          </div>

          {/* Selected region info */}
          {selected ? (
            <div className="brutalist-card p-5 bg-bee-yellow">
              <h4 className="text-2xl font-black uppercase mb-2">
                {selected.name}
              </h4>
              <p className="font-bold text-stone-700 mb-3">
                {selected.description}
              </p>
              <div className="bg-white border-4 border-black p-3">
                <p className="text-3xl font-black text-bee-rose">
                  ~{selected.production.toLocaleString()}
                </p>
                <p className="font-bold text-sm">tonnes/year</p>
              </div>
              <button 
                onClick={() => setSelectedRegion(null)}
                className="mt-3 text-sm font-bold underline"
              >
                Close
              </button>
            </div>
          ) : (
            <div className="brutalist-card p-4 bg-stone-100">
              <p className="font-bold text-stone-600 text-center">
                Select a region from the map to see details
              </p>
            </div>
          )}
        </div>
      </div>
    </div>
  );
}